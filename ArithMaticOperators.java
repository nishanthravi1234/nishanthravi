package ab55;

public class ArithMaticOperators {

	public static void main(String[] args) 
	{
	    int a=20;
	    int b=40;
	    int c=a+b;
	    System.out.println(c);
	    
	    
	    int a1=20;
	    int a2=40;
	    int c1=a1-a2;
	    System.out.println(c1);

	    
	    int a3=20;
	    int a4=40;
	    int c4=a3*a4;
	    System.out.println(c4);
	    
	    
	    int d=20;
	    int e=40;
	    int f=e%d;
	    System.out.println(f);
	    
	    
	    int a5=20;
	    int a6=40;
	    int c9=a6/a5;
	    System.out.println(c9);
	}

	}
	
	
	
	
	
	

